    private void formInternalFrameActivated(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameActivated
       Session sess = Session.getInstance();

if (sess.getU_id() == 0) {
    JOptionPane.showMessageDialog(null, "No Account. Login First!");
    LoginForm lf = new LoginForm();
    lf.setVisible(true);
    this.dispose();
} else {
    u_type.setText(sess.getU_type());
    fname.setText(sess.getU_fname());
    lname.setText(sess.getU_lname());
    email.setText(sess.getU_email());
    contact.setText(sess.getU_contact());
    gender.setText(sess.getU_gender());

    
}

    }//GEN-LAST:event_formInternalFrameActivated

    private void logoutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_logoutMouseClicked
    Session sess = Session.getInstance();
    if (sess.getU_id() != 0) {
        logAction(sess.getU_id(), "Logout");
    }
    LoginForm lf = new LoginForm();
    lf.setVisible(true);   
    JFrame parent = (JFrame) SwingUtilities.getWindowAncestor(this);
    parent.dispose();   
    }//GEN-LAST:event_logoutMouseClicked


